void Interpolate::nearest(const std::vector<Point> &pts, const BoundBox *bbox, std::vector<double> &ivals)
{
	try {act_box->contains(bbox);} catch (Bound_Fail) {throw Intrp_Fail();}

	int rel_x = 0;
	int rel_y = 0;
	int rel_z = 0;

	for (unsigned int i=0; i<pts.size(); i++) {
		rel_x = pts[i].ix() - est_box->min().ix();
		rel_y = pts[i].iy() - est_box->min().iy();
		rel_z = pts[i].iz() - est_box->min().iz();

		ivals[i] = kern_4d->get(rel_x,rel_y,rel_z,0);
	}
}
/******************************************************************************/
void Interpolate::tri_lin(const std::vector<Point> &pts, const BoundBox *bbox, std::vector<double> &ivals)
{
	try {act_box->contains(bbox);} catch (Bound_Fail) {throw Intrp_Fail();}

	int cx = 0;	// corner of the interp cell
	int cy = 0;
	int cz = 0;

	double rx = 0.0;	// relative position within interp cell
	double ry = 0.0;
	double rz = 0.0;

	for (unsigned int i=0; i<pts.size(); i++) {
		cx = pts[i].ix() - est_box->min().ix();
		cy = pts[i].iy() - est_box->min().iy();
		cz = pts[i].iz() - est_box->min().iz();

		rx = pts[i].rx();
		ry = pts[i].ry();
		rz = pts[i].rz();

		ivals[i] = kern_4d->get(cx+0,cy+0,cz+0,0)*(1.0-rx)*(1.0-ry)*(1.0-rz) +

			   kern_4d->get(cx+1,cy+0,cz+0,0)*(  rx  )*(1.0-ry)*(1.0-rz) +
			   kern_4d->get(cx+0,cy+1,cz+0,0)*(1.0-rx)*(  ry  )*(1.0-rz) +
			   kern_4d->get(cx+0,cy+0,cz+1,0)*(1.0-rx)*(1.0-ry)*(  rz  ) +

			   kern_4d->get(cx+1,cy+0,cz+1,0)*(  rx  )*(1.0-ry)*(  rz  ) +
			   kern_4d->get(cx+0,cy+1,cz+1,0)*(1.0-rx)*(  ry  )*(  rz  ) +
			   kern_4d->get(cx+1,cy+1,cz+0,0)*(  rx  )*(  ry  )*(1.0-rz) +

			   kern_4d->get(cx+1,cy+1,cz+1,0)*(  rx  )*(  ry  )*(  rz  );
	}
}
/******************************************************************************/
void Interpolate::tri_cub_Lek(const std::vector<Point> &pts, const BoundBox *bbox, std::vector<double> &ivals)
{
	try {act_box->contains(bbox);} catch (Bound_Fail) {throw Intrp_Fail();}

	for (unsigned int n=0; n<pts.size(); n++) {

		int cx = pts[n].ix() - est_box->min().ix();
		int cy = pts[n].iy() - est_box->min().iy();
		int cz = pts[n].iz() - est_box->min().iz();

		if (!kern_4d->get_Lc_avail(cx,cy,cz))	// calc if needed
			kernels_Lekien_one(cx,cy,cz);	// at the point

		double rx = pts[n].rx();
		double ry = pts[n].ry();
		double rz = pts[n].rz();

		ivals[n] = 0.0;

		for (int i=0; i<=3; i++)
		for (int j=0; j<=3; j++)
		for (int k=0; k<=3; k++) {

			// see Lekien paper
			int aijk = kern_4d->Lek_offset() + i + 4*j + 16*k;
			double alpha = kern_4d->get(cx,cy,cz,aijk);
			ivals[n] += alpha*pow(rx,i)*pow(ry,j)*pow(rz,k);
		}
	}
}
