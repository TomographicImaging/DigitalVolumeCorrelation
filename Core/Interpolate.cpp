void Interpolate::kernels(std::string voxfname, BoundBox *vox_box, int bytes_per, std::string endian, unsigned int offset)

// Load values from a voxel file. Derivatives are calculated at each voxel, but
// Lekien coeff's are not automatically evaluated (flagged to false to reflect new
// data in the interpolation region). The lekien coeff's are only evaluated for
// specific cells in the interp region as needed during tri_cub_Lek.

{
	std::ifstream vfs;
	vfs.open(voxfname.c_str(), std::ifstream::in | std::ifstream::binary);
	if (!vfs){std::cout <<"\n-> Can't open " << voxfname << "\n\n";return;}

	int vox_wide = vox_box->iwide();
	int vox_high = vox_box->ihigh();

	int est_box_min_x = est_box->min().ix();
	int est_box_min_y = est_box->min().iy();
	int est_box_min_z = est_box->min().iz();

	int voxls_in_row = est_box->iwide();
	int bytes_in_row = bytes_per*voxls_in_row;

	char *row_seg = new char [bytes_in_row];	// storage space for bytes read

   	char swap_val[] = {0,0};
   	unsigned short *u_short = (unsigned short*) swap_val;

	unsigned int file_pos = offset;
	vfs.seekg (file_pos);

	for (int is=0; is<est_box->itall(); is++) {
		for (int ir=0; ir<est_box->ihigh(); ir++) {

//			file_pos = bytes_per*((is + est_box->min().iz())*vox_wide*vox_high
//				 + (ir + est_box->min().iy())*vox_wide
//				       + est_box->min().ix());

			file_pos = bytes_per*((is + est_box_min_z)*vox_wide*vox_high
				 + (ir + est_box_min_y)*vox_wide
				       + est_box_min_x);

			vfs.seekg (file_pos);
			vfs.read (row_seg, bytes_in_row);	// read a row

			if (bytes_per == 1)	// 8-bit images
			{
				for (int ic=0; ic<voxls_in_row; ic++)
				{
					kern_4d->set(ic,ir,is,0,(unsigned char)row_seg[ic]);

					kern_4d->set_Lc_avail(ic,ir,is,false);

				}
			}

			if (bytes_per == 2)	// 16-bit images
			{
   				for (int ic=0; ic<voxls_in_row; ic++)
				{
					if (endian == "big")	// swap
					{
						swap_val[0] = row_seg[bytes_per*ic+1];
						swap_val[1] = row_seg[bytes_per*ic];
					}

					if (endian == "little")	// no swap
					{
						swap_val[1] = row_seg[bytes_per*ic+1];
						swap_val[0] = row_seg[bytes_per*ic];
					}

					kern_4d->set(ic,ir,is,0,u_short[0]);

					kern_4d->set_Lc_avail(ic,ir,is,false);

				}
			}

		}
	}

	vfs.close();

	kernels_derivs();
}
/******************************************************************************/
void Interpolate::kernels_derivs()
// order = f, f'(x), f'(y), f'(z), f''(xy), f''(xz), f''(yz), f'''(xyz)
//	   [0] [1]    [2]    [3]    [4]      [5]      [6]      [7]
{
	// interior of the box, where central diff's are possible
	for (int is=1; is<est_box->itall()-1; is++)
	for (int ir=1; ir<est_box->ihigh()-1; ir++)
	for (int ic=1; ic<est_box->iwide()-1; ic++) {

		// see Matrix_4d class for this, and be careful ...
		int c0 = ic*kern_4d->siz_y()*kern_4d->siz_z()*kern_4d->siz_k()
		       + ir*kern_4d->siz_z()*kern_4d->siz_k()
		       + is*kern_4d->siz_k();

		kern_4d->set_c0_fsp(c0);	// set single partial terms
		kern_4d->set_c0_fdp(c0);	// set double partial terms
		kern_4d->set_c0_ftp(c0);	// set triple partial term
	}

	// deal with the x-faces, copying in until for/back diff's are done
	for (int is=0; is<est_box->itall(); is++)
	for (int ir=0; ir<est_box->ihigh(); ir++)
	for (int n=1; n<8; n++) {
		kern_4d->set(0, ir, is, n, kern_4d->get(1, ir, is, n));
		kern_4d->set(est_box->iwide()-1, ir, is, n, kern_4d->get(est_box->iwide()-2, ir, is, n));
	}

	// deal with the y-faces, copying in until for/back diff's are done
	for (int is=0; is<est_box->itall(); is++)
	for (int ic=0; ic<est_box->iwide(); ic++)
	for (int n=1; n<8; n++) {
		kern_4d->set(ic, 0, is, n, kern_4d->get(ic, 1, is, n));
		kern_4d->set(ic, est_box->ihigh()-1, is, n, kern_4d->get(ic, est_box->ihigh()-2, is, n));
	}

	// deal with the z-faces, copying in until for/back diff's are done
	for (int ir=0; ir<est_box->ihigh(); ir++)
	for (int ic=0; ic<est_box->iwide(); ic++)
	for (int n=1; n<8; n++) {
		kern_4d->set(ic, ir, 0, n, kern_4d->get(ic, ir, 1, n));
		kern_4d->set(ic, ir, est_box->itall()-1, n, kern_4d->get(ic, ir, est_box->itall()-2, n));
	}
}
/******************************************************************************/
void Interpolate::kernels_Lekien_all()
// Not routinely done, but a possibility for very tight interp regions and dense
// floating clouds.
{
 	Eigen::VectorXd va(64);	// the coveted Lekien coefficients!
 	Eigen::VectorXd vb(64);	// interp cube corner values

	// all the interp cubes with full corner information
	// need deriv's past current location, thus the -1 index limit
	for (int is=0; is<est_box->itall()-1; is++)
	for (int ir=0; ir<est_box->ihigh()-1; ir++)
	for (int ic=0; ic<est_box->iwide()-1; ic++) {

		// see Matrix_4d class for this, and be careful ...
		int c0 = ic*kern_4d->siz_y()*kern_4d->siz_z()*kern_4d->siz_k()
		       + ir*kern_4d->siz_z()*kern_4d->siz_k()
		       + is*kern_4d->siz_k();

		// load the derivative kernels used in Lekien coeff calculation
		for (int k=0; k<8; k++)
			kern_4d->load_vb_k(vb, c0, k);

		// matrix multiply, sparse optimized through Eigen
		// still an intensive part of the calculation
 		va = sBI*vb;

 		// load the Lekien coefficients back into the kernel structure
		for (int n=0; n<64; n++)
			kern_4d->set_c0_k(c0,n+8,va(n));

		kern_4d->set_Lc_avail(ic,ir,is,true);
	}

	// ought to deal with the box max faces here

}
/******************************************************************************/
void Interpolate::kernels_Lekien_one(int ic, int ir, int is)
{
 	Eigen::VectorXd va(64);	// the coveted Lekien coefficients!
 	Eigen::VectorXd vb(64);	// interp cube corner values

	// see Matrix_4d class for this, and be careful ...
	int c0 = ic*kern_4d->siz_y()*kern_4d->siz_z()*kern_4d->siz_k()
	       + ir*kern_4d->siz_z()*kern_4d->siz_k()
	       + is*kern_4d->siz_k();

	// load the derivative kernels used in Lekien coeff calculation
	for (int k=0; k<8; k++)
		kern_4d->load_vb_k(vb, c0, k);

	// matrix multiply, sparse optimized through Eigen
	// still an intensive part of the calculation
	va = sBI*vb;

	// load the Lekien coefficients back into the kernel structure
	for (int n=0; n<64; n++)
		kern_4d->set_c0_k(c0,n+8,va(n));

	// don't calculate again if same cell is entered, just retrieve
	kern_4d->set_Lc_avail(ic,ir,is,true);
}
